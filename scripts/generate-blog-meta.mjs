import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const POSTS_DIR = path.join(__dirname, '..', 'src', 'posts');
const PUBLIC_DIR = path.join(__dirname, '..', 'public');
const META_OUTPUT = path.join(__dirname, '..', 'src', 'posts', 'posts-meta.js');

function parseFrontmatter(fileContent) {
  const match = fileContent.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) {
    return { frontmatter: {}, content: fileContent };
  }
  const yamlBlock = match[1];
  const content = match[2];
  const frontmatter = {};

  yamlBlock.split('\n').forEach(line => {
    const colonIndex = line.indexOf(':');
    if (colonIndex !== -1) {
      const key = line.substring(0, colonIndex).trim();
      let val = line.substring(colonIndex + 1).trim();
      
      // Clean quotes
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.substring(1, val.length - 1);
      }
      
      // Parse arrays e.g., ["ai", "python"]
      if (val.startsWith('[') && val.endsWith(']')) {
        try {
          // Replace single quotes with double quotes for valid JSON
          const jsonVal = val.replace(/'/g, '"');
          frontmatter[key] = JSON.parse(jsonVal);
        } catch (e) {
          frontmatter[key] = val.slice(1, -1).split(',').map(s => s.trim().replace(/^["']|["']$/g, ''));
        }
      } else {
        frontmatter[key] = val;
      }
    }
  });

  return { frontmatter, content };
}

function generate() {
  console.log('📝 Generating Blog Metadata, Sitemaps & RSS Feed...');

  if (!fs.existsSync(POSTS_DIR)) {
    fs.mkdirSync(POSTS_DIR, { recursive: true });
  }

  const files = fs.readdirSync(POSTS_DIR);
  const posts = [];

  files.forEach(file => {
    if (file.endsWith('.md')) {
      const filePath = path.join(POSTS_DIR, file);
      const fileContent = fs.readFileSync(filePath, 'utf-8');
      const { frontmatter, content } = parseFrontmatter(fileContent);

      if (frontmatter.slug && frontmatter.title) {
        // Calculate read time roughly: ~200 words per minute
        const words = content.trim().split(/\s+/).length;
        const readTime = Math.ceil(words / 200) + ' min read';

        posts.push({
          title: frontmatter.title,
          slug: frontmatter.slug,
          date: frontmatter.date || new Date().toISOString().split('T')[0],
          excerpt: frontmatter.excerpt || '',
          coverImage: frontmatter.coverImage || '',
          tags: frontmatter.tags || [],
          readTime,
          content: content.trim()
        });
      }
    }
  });

  // Sort posts by date descending
  posts.sort((a, b) => new Date(b.date) - new Date(a.date));

  // Write posts-meta.js (this exports the post metadata array + contents for dynamic import)
  const metaContent = `// Auto-generated by generate-blog-meta.mjs - DO NOT EDIT DIRECTLY\nexport const blogPosts = ${JSON.stringify(posts, null, 2)};\n`;
  fs.writeFileSync(META_OUTPUT, metaContent, 'utf-8');
  console.log(`✅ Generated posts metadata code at ${META_OUTPUT}`);

  // Generate public/sitemap.xml
  const sitemapXml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
  <!-- Homepage -->
  <url>
    <loc>https://hamzax.me/</loc>
    <changefreq>weekly</changefreq>
    <priority>1.0</priority>
  </url>
  <!-- Blog List Page -->
  <url>
    <loc>https://hamzax.me/blog</loc>
    <changefreq>weekly</changefreq>
    <priority>0.9</priority>
  </url>
  <!-- Individual Blog Posts -->
${posts.map(post => `  <url>
    <loc>https://hamzax.me/blog/${post.slug}</loc>
    <changefreq>monthly</changefreq>
    <priority>0.8</priority>
  </url>`).join('\n')}
</urlset>`;

  fs.writeFileSync(path.join(PUBLIC_DIR, 'sitemap.xml'), sitemapXml, 'utf-8');
  console.log(`✅ Generated public/sitemap.xml`);

  // Generate public/image-sitemap.xml
  const imageSitemapXml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">
  <!-- Homepage -->
  <url>
    <loc>https://hamzax.me/</loc>
    <image:image>
      <image:loc>https://hamzax.me/images/muhammad-hamza-profile.jpg</image:loc>
      <image:title>Muhammad Hamza — Software Engineer from Peshawar, Pakistan</image:title>
      <image:caption>Muhammad Hamza, Software Engineer, Main Developer of Quizior, Co-Founder at Solvia Codes, Co-Lead at Nexior.</image:caption>
    </image:image>
    <image:image>
      <image:loc>https://hamzax.me/favicon-512x512.png</image:loc>
      <image:title>Muhammad Hamza — Portfolio Logo</image:title>
    </image:image>
  </url>
  <!-- Blog Page -->
  <url>
    <loc>https://hamzax.me/blog</loc>
  </url>
  <!-- Blog Posts -->
${posts.map(post => `  <url>
    <loc>https://hamzax.me/blog/${post.slug}</loc>
${post.coverImage ? `    <image:image>
      <image:loc>https://hamzax.me${post.coverImage}</image:loc>
      <image:title>${post.title}</image:title>
      <image:caption>${post.excerpt}</image:caption>
    </image:image>` : ''}  </url>`).join('\n')}
</urlset>`;

  fs.writeFileSync(path.join(PUBLIC_DIR, 'image-sitemap.xml'), imageSitemapXml, 'utf-8');
  console.log(`✅ Generated public/image-sitemap.xml`);

  // Generate public/feed.xml (RSS Feed)
  const rssXml = `<?xml version="1.0" encoding="UTF-8" ?>
<rss version="2.0" xmlns:atom="http://www.w3.org/2005/Atom">
<channel>
  <title>Muhammad Hamza's Blog</title>
  <link>https://hamzax.me/blog</link>
  <description>Articles on software engineering, AI/ML, and building products like Quizior, JARVIS, and more — written by Muhammad Hamza.</description>
  <language>en-us</language>
  <pubDate>${new Date().toUTCString()}</pubDate>
  <lastBuildDate>${new Date().toUTCString()}</lastBuildDate>
  <atom:link href="https://hamzax.me/feed.xml" rel="self" type="application/rss+xml" />
${posts.map(post => `  <item>
    <title>${post.title}</title>
    <link>https://hamzax.me/blog/${post.slug}</link>
    <description>${post.excerpt || 'Read the full post on Muhammad Hamza\'s website.'}</description>
    <pubDate>${new Date(post.date).toUTCString()}</pubDate>
    <guid>https://hamzax.me/blog/${post.slug}</guid>
  </item>`).join('\n')}
</channel>
</rss>`;

  fs.writeFileSync(path.join(PUBLIC_DIR, 'feed.xml'), rssXml, 'utf-8');
  console.log(`✅ Generated public/feed.xml (RSS Feed)`);
}

generate();
